#include "GameNet/BombBuffer.h"

CBombBuffer::CBombBuffer(size_t initsize, size_t delta) : Net::CBuffer(initsize, delta)
{
}

CBombBuffer::~CBombBuffer()
{
}

void CBombBuffer::write(const FVector &_data)
{
    write(_data.X);
    write(_data.Y);
    write(_data.Z);
}
void CBombBuffer::read(FVector &data_)
{
    read(data_.X);
    read(data_.Y);
    read(data_.Z);
}
